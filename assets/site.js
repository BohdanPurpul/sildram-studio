async function loadPublicConfig() {
    try {
        const response = await fetch("/api/config");
        if (!response.ok) return;
        const config = await response.json();
        turnstileSiteKey = config.turnstileSiteKey || "";
    } catch (error) {
        turnstileSiteKey = "";
    }
}

function setupCaptcha(container, onToken) {
    if (!container) return;

    if (!turnstileSiteKey) {
        container.dataset.captchaState = "demo";
        container.textContent = translateValue("CAPTCHA активується після додавання Turnstile ключів на сервері.");
        return;
    }

    container.textContent = "";

    const render = () => {
        if (!window.turnstile) return;
        const widgetId = window.turnstile.render(container, {
            sitekey: turnstileSiteKey,
            theme: "dark",
            callback: onToken,
            "expired-callback": () => onToken(""),
            "error-callback": () => onToken("")
        });
        container.dataset.widgetId = widgetId;
        container.dataset.captchaState = "ready";
    };

    if (window.turnstile) {
        render();
        return;
    }

    const existing = document.querySelector("script[data-turnstile]");
    if (existing) {
        existing.addEventListener("load", render, { once: true });
        return;
    }

    const script = document.createElement("script");
    script.src = "https://challenges.cloudflare.com/turnstile/v0/api.js?render=explicit";
    script.async = true;
    script.defer = true;
    script.dataset.turnstile = "true";
    script.addEventListener("load", render, { once: true });
    document.head.appendChild(script);
}

